CREATE OR REPLACE PROCEDURE PROCESS_PRODUCT_CHANGES()
RETURNS VARCHAR
LANGUAGE SQL
AS
$$
BEGIN
    MERGE INTO
        LEARNING_DB.PUBLIC.PRODUCTS_FINAL PF USING (
            SELECT
                PS.PRODUCT_ID,
                PS.PRODUCT_NAME,
                PS.PRODUCT_PRICE,
                PS.STORE_ID,
                S.STORE_NAME,
                S.LOCATION,
                PS.METADATA$ACTION,
                PS.METADATA$ISUPDATE
            FROM
                LEARNING_DB.PUBLIC.PRODUCTS_STREAM PS
                JOIN LEARNING_DB.PUBLIC.STORES S ON PS.STORE_ID = S.STORE_ID
        ) FPS ON PF.PRODUCT_ID = FPS.PRODUCT_ID
    WHEN MATCHED AND
        FPS.METADATA$ACTION = 'INSERT'
        AND FPS.METADATA$ISUPDATE = 'TRUE'
    THEN UPDATE SET
        PF.PRODUCT_NAME = FPS.PRODUCT_NAME,
        PF.PRODUCT_PRICE = FPS.PRODUCT_PRICE,
        PF.STORE_ID = FPS.STORE_ID
    WHEN MATCHED AND
        FPS.METADATA$ACTION = 'DELETE'
        AND FPS.METADATA$ISUPDATE = 'FALSE'
    THEN DELETE
    WHEN NOT MATCHED AND
        FPS.METADATA$ACTION = 'INSERT'
        AND FPS.METADATA$ISUPDATE = 'FALSE'
    THEN INSERT (
            PRODUCT_ID,
            PRODUCT_NAME,
            PRODUCT_PRICE,
            STORE_ID,
            STORE_NAME,
            LOCATION
        )
        VALUES
            (
                FPS.PRODUCT_ID,
                FPS.PRODUCT_NAME,
                FPS.PRODUCT_PRICE,
                FPS.STORE_ID,
                FPS.STORE_NAME,
                FPS.LOCATION
            );

    RETURN 'Merge completed successfully';
END;
$$;

