-- Data masking policy
CREATE
OR REPLACE
MASKING POLICY
    PHONE AS (VAL VARCHAR) RETURNS VARCHAR -> CASE
        WHEN CURRENT_ROLE() in (ACCOUNT_ADMIN, SYSADMIN) THEN VAL
        ELSE LEFT(VAL, 2) || "#-###-###"
    END;

DESC MASKING POLICY PHONE;

SHOW MASKING POLICIES;

ALTER TABLE CUSTOMERS MODIFY COLUMN PHONE
SET MASKING POLICY PHONE;

ALTER TABLE CUSTOMERS MODIFY COLUMN PHONE
UNSET MASKING POLICY;

ALTER MASKING POLICY PHONE
SET
    BODY -> CASE
        WHEN CURRENT_ROLE() in (ACCOUNT_ADMIN, SYSADMIN) THEN VAL
        ELSE CONCAT(LEFT(VAL, 2), "#-###-###")
    END;

SELECT * FROM TABLE(INFORMATION_SCHEMA.POLICY_REFERENCES(POLICY_NAME => 'PHONE'));

-- Access management

--      +--ACCOUNTADMIN--+
--      ^                ^
--      |                |
-- SECURITYADMIN      SYSADMIN
--      ^                ^
--      |                |
--  USERADMIN       <CUSTOM_ROLE>
--      ^
--      |
--   PUBLIC

-- Types of Access Control:
-- 1. Discretionary access control (DAC) ie owner decides who can access
-- 2. Role based access control (RBAC) ie role decides who can access

-- Grant privilege on object to role
GRANT <PRIVILEGE>
ON <OBJECT_TYPE> <OBJECT_NAME>
TO ROLE <ROLE>;

-- Grant privilege on object to share
GRANT <PRIVILEGE>
ON <OBJECT_TYPE> <OBJECT_NAME>
TO SHARE <SHARE_NAME>;


-- Grant role to user
GRANT ROLE <ROLE>
TO USER <USER>;

-- Grant role to role
GRANT ROLE <ROLE>
TO ROLE <ROLE>;

CREATE ROLE <ROLE_NAME>;

CREATE USER john PASSWORD = '123'
DEFAULT ROLE ACCOUNTADMIN
MUST_CHANGE_PASSWORD = TRUE;

GRANT ROLE ACCOUNTADMIN
TO USER john;
